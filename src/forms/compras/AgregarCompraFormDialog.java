     public boolean validateForm(){
         
         StringBuilder message = new StringBuilder();
         int cont = 0;
         
         if(this.txtCantidad.getText().equals("")){
             message.append(++cont);
             message.append(". Cantidad no puede ir vacio\n");
         }
         if(this.txtPrecioCompra.getText().equals("")){
             message.append(++cont);
             message.append(". Precio compra no puede ir vacio\n");
         }
       
         if(!message.toString().equals("")){
             JOptionPane.showMessageDialog(null, message.toString(), "Error", JOptionPane.ERROR_MESSAGE);
             return false;
         }else{
             return true;
         }  
     
     }
    
    
