    public void llenar_tabla_compras(Integer limit){
        formato_tabla_compras();
        List<Compra> compras = comprasService.obtenerComprasPorArticuloId(false, articulo.getArticuloId(),limit);
        
        if(compras == null || compras.size()<0){
            return;
        }
        for(Compra compra : compras){
           
            DefaultTableModel temp = (DefaultTableModel) tablaCompras.getModel();
            
            Object fila[] = {
                compra.getId(),
                compra.getIdArticulo(),
                compra.getCantidad(),
                compra.getPrecioCompra(),
                compra.getComentario(),
                compra.getCreado(),
                compra.getActualizado()
            };
            temp.addRow(fila);
        } // end for compras
        
    }
    
     public void formato_tabla_compras(){
        Object[][] data = {{"","","","", "","",""}};
        String[] columNames = {"id","articuloId", "cantidad","precio compra","comentario","creado","actualizado"};
        DefaultTableModel tableModel = new DefaultTableModel(data, columNames);
        tablaCompras.setModel(tableModel);
        
        TableRowSorter<TableModel> ordenarTabla = new TableRowSorter<TableModel>(tableModel); 
        tablaCompras.setRowSorter(ordenarTabla);
        
        int[] anchos = {10,10, 30,30,100,40,40};

        for (int inn = 0; inn < tablaCompras.getColumnCount(); inn++) {
            tablaCompras.getColumnModel().getColumn(inn).setPreferredWidth(anchos[inn]);
        }
        
         try {
            DefaultTableModel temp = (DefaultTableModel) tablaCompras.getModel();
            temp.removeRow(temp.getRowCount() - 1);
        } catch (ArrayIndexOutOfBoundsException e) {
            ;
        }      
     
        DefaultTableCellRenderer centrar = new DefaultTableCellRenderer();
        centrar.setHorizontalAlignment(SwingConstants.CENTER);
        
        tablaCompras.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaCompras.getColumnModel().getColumn(0).setMinWidth(0);
        tablaCompras.getColumnModel().getColumn(0).setPreferredWidth(0);
        
         tablaCompras.getColumnModel().getColumn(1).setMaxWidth(0);
        tablaCompras.getColumnModel().getColumn(1).setMinWidth(0);
        tablaCompras.getColumnModel().getColumn(1).setPreferredWidth(0);
      
        tablaCompras.getColumnModel().getColumn(2).setCellRenderer(centrar);
        tablaCompras.getColumnModel().getColumn(3).setCellRenderer(centrar);
        tablaCompras.getColumnModel().getColumn(4).setCellRenderer(centrar);
        tablaCompras.getColumnModel().getColumn(5).setCellRenderer(centrar);
        tablaCompras.getColumnModel().getColumn(6).setCellRenderer(centrar);
      
      
    }
     
     

