package model;

/**
 *
 * @author jerry
 */
public class Abono {
    
    private int abonoId;
    private int rentaId;
    private int usuarioId;
    private String fecha;
    private float abono;
    private String comentario;
    private TipoAbono tipoAbono;
    private String fechaPago;
    private Renta renta;
    private Usuario usuario;
    private Float totalAbonos;

    public Float getTotalAbonos() {
        return totalAbonos;
    }

    public void setTotalAbonos(Float totalAbonos) {
        this.totalAbonos = totalAbonos;
    }
    
    

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }
    
    

    public Renta getRenta() {
        return renta;
    }

    public void setRenta(Renta renta) {
        this.renta = renta;
    }
    
    

    public TipoAbono getTipoAbono() {
        return tipoAbono;
    }

    public void setTipoAbono(TipoAbono tipoAbono) {
        this.tipoAbono = tipoAbono;
    }

    
    public String getFechaPago() {
        return fechaPago;
    }

    public void setFechaPago(String fechaPago) {
        this.fechaPago = fechaPago;
    }

    
    public int getAbonoId() {
        return abonoId;
    }

    public void setAbonoId(int abonoId) {
        this.abonoId = abonoId;
    }

    public int getRentaId() {
        return rentaId;
    }

    public void setRentaId(int rentaId) {
        this.rentaId = rentaId;
    }

    public int getUsuarioId() {
        return usuarioId;
    }

    public void setUsuarioId(int usuarioId) {
        this.usuarioId = usuarioId;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public float getAbono() {
        return abono;
    }

    public void setAbono(float abono) {
        this.abono = abono;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    @Override
    public String toString() {
        return "Abono{" + "abonoId=" + abonoId + ", rentaId=" + rentaId + ", usuarioId=" + usuarioId + ", fecha=" + fecha + ", abono=" + abono + ", comentario=" + comentario + ", tipoAbono=" + tipoAbono + ", fechaPago=" + fechaPago + '}';
    }
    
    
    
    
}
