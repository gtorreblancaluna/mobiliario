package model.material.inventory;

public class MaterialSaleItemReport {
    
    
    private Float amount;
    private String measurementUnitDescription;
    private String materialInventoryDescription;
    private Float purchaseAmount;
    private Float purchaseAmountRound;
    private String purchaseMeasurementUnitDescription;
    private String providerName;
    private String providerAddress;
    private String providerPhoneNumber;
    private String descriptionItem;
    private Float amountItem;

    public Float getAmount() {
        return amount;
    }

    public void setAmount(Float amount) {
        this.amount = amount;
    }

    public String getMeasurementUnitDescription() {
        return measurementUnitDescription;
    }

    public void setMeasurementUnitDescription(String measurementUnitDescription) {
        this.measurementUnitDescription = measurementUnitDescription;
    }

    public String getMaterialInventoryDescription() {
        return materialInventoryDescription;
    }

    public void setMaterialInventoryDescription(String materialInventoryDescription) {
        this.materialInventoryDescription = materialInventoryDescription;
    }

    public Float getPurchaseAmount() {
        return purchaseAmount;
    }

    public void setPurchaseAmount(Float purchaseAmount) {
        this.purchaseAmount = purchaseAmount;
    }

    public Float getPurchaseAmountRound() {
        return purchaseAmountRound;
    }

    public void setPurchaseAmountRound(Float purchaseAmountRound) {
        this.purchaseAmountRound = purchaseAmountRound;
    }

    public String getPurchaseMeasurementUnitDescription() {
        return purchaseMeasurementUnitDescription;
    }

    public void setPurchaseMeasurementUnitDescription(String purchaseMeasurementUnitDescription) {
        this.purchaseMeasurementUnitDescription = purchaseMeasurementUnitDescription;
    }

    public String getProviderName() {
        return providerName;
    }

    public void setProviderName(String providerName) {
        this.providerName = providerName;
    }

    public String getProviderAddress() {
        return providerAddress;
    }

    public void setProviderAddress(String providerAddress) {
        this.providerAddress = providerAddress;
    }

    public String getProviderPhoneNumber() {
        return providerPhoneNumber;
    }

    public void setProviderPhoneNumber(String providerPhoneNumber) {
        this.providerPhoneNumber = providerPhoneNumber;
    }

    public String getDescriptionItem() {
        return descriptionItem;
    }

    public void setDescriptionItem(String descriptionItem) {
        this.descriptionItem = descriptionItem;
    }

    public Float getAmountItem() {
        return amountItem;
    }

    public void setAmountItem(Float amountItem) {
        this.amountItem = amountItem;
    }
    
    
    
   

}
